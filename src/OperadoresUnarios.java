public class OperadoresUnarios {
    public static void main(String[] args) {

        int i = -5;

        int j = +i; // j = (1) * i = -5
        System.out.println("j = " + j);

        int k = -i; // k = (-1) * i = 5;
        System.out.println("k = " + k);

        float kk = 12;
        float l = -kk;
        System.out.println("l = " + l);
    }
}
